# What is the difference between big endian and little endian?

"In computing, endianness is the order or sequence of bytes of a word of digital data in computer memory."
"Endianness is primarily expressed as big-endian or little-endian. A big-endian system stores the most significant"
"byte of a word at the smallest/first memory address (as a person would read from left to right)"
"and the least significant byte at the largest/last."

"A little endian system will reverse the order and save the least significant word at first place"