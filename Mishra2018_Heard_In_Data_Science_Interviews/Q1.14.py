# What is the value of 15^18^15?

print(" ^ = Bitwise Exclusive XOR - the XOR Trick \n \
A bitwise XOR is a binary operation that takes two bit patterns of equal \n \
length and performs the logical exclusive OR operation on each pair of corresponding bits.\n \
The result in each position is 1 if only one of the bits is 1, but will be 0 if both are 0 or both are 1.\n \
")

print("The result of 15^18^15 is ", 15^18^15)

print("The result of 15^15^15 is ", 15^15^15, "\nEqual values are canceled out by XOR - 15 in the middle remains.")

