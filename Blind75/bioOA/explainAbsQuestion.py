# Answer:
# The abs() function in Python is used to calculate the absolute value of a number. The absolute value of a number is its distance from zero on the number line, regardless of direction.

# For example:
#    abs(-5) returns 5
#    abs(5) also returns 5

# It is commonly used in scenarios where only the magnitude of a value matters and negative values have to be excluded.
